class ValidOrderingTests(unittest.TestCase):
    """Valid sequences should follow defined ordering rules."""

    def test_happy_path_starts_with_command(self) -> None:
        self.assertEqual(VALID_SEQUENCES["happy_path"][0], "dispatch.command")

    def test_happy_path_ends_with_result(self) -> None:
        self.assertEqual(VALID_SEQUENCES["happy_path"][-1], "worker.result")

    def test_timeout_path_has_no_ack(self) -> None:
        self.assertNotIn("dispatch.ack", VALID_SEQUENCES["timeout_path"])

    def test_timeout_path_ends_with_noop(self) -> None:
        self.assertEqual(VALID_SEQUENCES["timeout_path"][-1], "dispatch.noop")

    def test_ack_then_error_has_ack_before_error(self) -> None:
        seq = VALID_SEQUENCES["ack_then_error"]
        self.assertLess(seq.index("dispatch.ack"), seq.index("dispatch.error"))

    def test_all_valid_sequences_start_with_command(self) -> None:
        for name, seq in VALID_SEQUENCES.items():
            self.assertEqual(seq[0], "dispatch.command",
                             f"valid sequence '{name}' must start with command")


