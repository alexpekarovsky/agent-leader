class CorrelationConsistencyTests(unittest.TestCase):
    """All fixtures in the pack share the same correlation_id and task_id."""

    def test_all_share_same_correlation_id(self) -> None:
        cid = FIXTURE_COMMAND["correlation_id"]
        for name, fixture in ALL_FIXTURES.items():
            self.assertEqual(fixture["correlation_id"], cid,
                             f"fixture '{name}' has different correlation_id")

    def test_all_share_same_task_id(self) -> None:
        tid = FIXTURE_COMMAND["task_id"]
        for name, fixture in ALL_FIXTURES.items():
            self.assertEqual(fixture["task_id"], tid,
                             f"fixture '{name}' has different task_id")

    def test_correlation_id_format(self) -> None:
        cid = FIXTURE_COMMAND["correlation_id"]
        self.assertTrue(cid.startswith("CMD-"), "correlation_id should start with CMD-")

    def test_timestamps_are_valid_iso8601(self) -> None:
        for name, fixture in ALL_FIXTURES.items():
            try:
                datetime.fromisoformat(fixture["timestamp"])
            except ValueError:
                self.fail(f"fixture '{name}' has invalid timestamp")

    def test_timestamps_are_chronological(self) -> None:
        """command < ack < result (happy path ordering by timestamp)."""
        cmd_ts = datetime.fromisoformat(FIXTURE_COMMAND["timestamp"])
        ack_ts = datetime.fromisoformat(FIXTURE_ACK["timestamp"])
        result_ts = datetime.fromisoformat(FIXTURE_RESULT["timestamp"])
        self.assertLess(cmd_ts, ack_ts)
        self.assertLess(ack_ts, result_ts)


