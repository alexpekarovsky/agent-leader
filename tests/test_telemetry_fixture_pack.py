# 5. Invalid ordering detection
# ---------------------------------------------------------------------------

class InvalidOrderingTests(unittest.TestCase):
    """Invalid sequences document violations that validators should catch."""

    def test_orphan_ack_has_no_command(self) -> None:
        seq = INVALID_SEQUENCES["orphan_ack"]
        self.assertNotIn("dispatch.command", seq)

    def test_result_before_ack_is_out_of_order(self) -> None:
        seq = INVALID_SEQUENCES["result_before_ack"]
        self.assertNotIn("dispatch.ack", seq)

    def test_noop_after_ack_is_contradictory(self) -> None:
        """Noop should not follow ack — ack means command was consumed."""
        seq = INVALID_SEQUENCES["noop_after_ack"]
        self.assertIn("dispatch.ack", seq)
        self.assertIn("dispatch.noop", seq)

    def test_duplicate_ack_has_two_acks(self) -> None:
        seq = INVALID_SEQUENCES["duplicate_ack"]
        self.assertEqual(seq.count("dispatch.ack"), 2)

    def test_invalid_sequences_are_distinct_from_valid(self) -> None:
        valid_set = set(tuple(s) for s in VALID_SEQUENCES.values())
        for name, seq in INVALID_SEQUENCES.items():
            self.assertNotIn(tuple(seq), valid_set,
                             f"invalid sequence '{name}' should not match any valid sequence")


