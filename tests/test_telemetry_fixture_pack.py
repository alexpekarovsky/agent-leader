class LiveCommandAckIntegrationTests(unittest.TestCase):
    """Live command→ack telemetry via set_claim_override + claim_next_task."""

    def test_command_event_emitted_on_override(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            orch = _make_orch(root)
            _setup_agent(orch, root, "claude_code")
            task = orch.create_task(
                title="Live cmd test",
                workstream="backend",
                owner="claude_code",
                acceptance_criteria=["test"],
            )
            orch.bus.events_path.write_text("", encoding="utf-8")

            orch.set_claim_override(agent="claude_code", task_id=task["id"], source="codex")

            events = list(orch.bus.iter_events())
            cmd_events = [e for e in events if e.get("type") == "dispatch.command"]
            self.assertEqual(len(cmd_events), 1)
            self.assertEqual(cmd_events[0]["payload"]["command_type"], "claim_override")

    def test_ack_event_emitted_on_claim(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            orch = _make_orch(root)
            _setup_agent(orch, root, "claude_code")
            task = orch.create_task(
                title="Live ack test",
                workstream="backend",
                owner="claude_code",
                acceptance_criteria=["test"],
            )
            override = orch.set_claim_override(
                agent="claude_code", task_id=task["id"], source="codex",
            )
            orch.bus.events_path.write_text("", encoding="utf-8")

            orch.claim_next_task("claude_code")

            events = list(orch.bus.iter_events())
            ack_events = [e for e in events if e.get("type") == "dispatch.ack"]
            self.assertEqual(len(ack_events), 1)
            self.assertEqual(ack_events[0]["payload"]["ack_type"], "claim_override_consumed")

    def test_command_and_ack_share_correlation_id(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            orch = _make_orch(root)
            _setup_agent(orch, root, "claude_code")
            task = orch.create_task(
                title="Live corr test",
                workstream="backend",
                owner="claude_code",
                acceptance_criteria=["test"],
            )
            orch.bus.events_path.write_text("", encoding="utf-8")

            override = orch.set_claim_override(
                agent="claude_code", task_id=task["id"], source="codex",
            )
            orch.claim_next_task("claude_code")

            events = list(orch.bus.iter_events())
            cmd = next(e for e in events if e.get("type") == "dispatch.command")
            ack = next(e for e in events if e.get("type") == "dispatch.ack")
            self.assertEqual(cmd["payload"]["correlation_id"], ack["payload"]["correlation_id"])

    def test_noop_emitted_on_stale_override(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            orch = _make_orch(root)
            _setup_agent(orch, root, "claude_code")
            task = orch.create_task(
                title="Live noop test",
                workstream="backend",
                owner="claude_code",
                acceptance_criteria=["test"],
            )
            orch.set_claim_override(
                agent="claude_code", task_id=task["id"], source="codex",
            )
            # Backdate override to trigger timeout
            overrides = orch._read_json(orch.claim_overrides_path)
            overrides["claude_code"]["created_at"] = "2020-01-01T00:00:00+00:00"
            orch._write_json(orch.claim_overrides_path, overrides)
            orch.bus.events_path.write_text("", encoding="utf-8")

            orch.emit_stale_claim_override_noops(source="codex", timeout_seconds=5)

            events = list(orch.bus.iter_events())
            noop_events = [e for e in events if e.get("type") == "dispatch.noop"]
            self.assertEqual(len(noop_events), 1)
            self.assertEqual(noop_events[0]["payload"]["reason"], "claim_override_timeout")


