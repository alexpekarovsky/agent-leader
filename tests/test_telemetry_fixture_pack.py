"""CORE-05 ack/noop/result/error telemetry fixture pack with ordering examples.

Provides realistic fixtures for all four dispatch telemetry payload types
and documents valid/invalid event ordering sequences.

Telemetry Payload Types
=======================
1. dispatch.command  — Manager directs agent to claim a specific task
2. dispatch.ack      — Agent acknowledges command (claim override consumed)
3. dispatch.noop     — Command timed out without ack (claim override stale)
4. worker.result     — Agent delivers task completion result (planned CORE-05)
5. dispatch.error    — Unrecoverable dispatch failure (planned CORE-05)

Valid Ordering Sequences
========================
A) Happy path:   command → ack → result
B) Timeout path:  command → noop  (no ack received within timeout)
C) Ack + failure: command → ack → error  (worker acked but failed)
D) Direct claim:  (no command) → task.claimed  (normal claim without override)

Invalid Ordering (should be flagged by validators):
X1) ack without preceding command  (orphan ack)
X2) result before ack              (result without acknowledgement)
X3) noop after ack                 (noop should be suppressed when ack exists)
X4) duplicate ack for same command (idempotency violation)

Correlation Rules
=================
- All events in a sequence share the same correlation_id (CMD-{hex})
- correlation_id is generated by set_claim_override and threaded through
  claim_next_task (ack) and emit_stale_claim_override_noops (noop)
- task_id must match across all events in the sequence
- source differs: command source=manager, ack source=worker, noop source=manager
"""

